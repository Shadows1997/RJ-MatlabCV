t=0:pi/5:2*pi;                   %设置t的取值范围及刻度
a=t(1:length(t)-1);           %确定变量a与t之间的关系
patch(sin(a),cos(a),1:length(a),'FaceColor','interp')
colormap cool;     %设置颜色表类型
axis equal          %调整坐标轴
