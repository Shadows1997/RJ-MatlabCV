 [x,y]=meshgrid(-1:0.1:1);   %设置矩形网络
z=x.*y+cos(x.*y.*pi);         %定义z与x、y之间的函数关系
c=randn(size(z));               %定义c为矩阵z的均匀分布伪随机数         
surf(x,y,z,c)                  %绘制网格曲面图
colormap(cool)                 %设置颜色表
colorbar                          %添加颜色条
