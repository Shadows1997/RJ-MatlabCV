x=0:0.05:10;                                               %定义x坐标轴范围及刻度
y=exp(-0.2*x).*cos(x);                                   %定义y与x之间的函数关系
plot(x,y)                                                    %绘制函数图形
title('\ite^{0.2x}cos(x)','FontWeight','Bold')    %为图形标注标题，并设置标题属性
xlabel('X')
ylabel('Y')

