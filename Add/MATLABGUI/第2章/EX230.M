%功能：等高线绘制函数使用实例
%****************************************************
z=peaks;                   %绘制peaks图形
subplot(2,1,1)           %子图设置
 contour(z)               %绘制peaks图形等高线图
subplot(2,1,2)           %子图设置
 [c,h]=contour(z,[3.8 1.5]);  
 clabel(c,h)		%标注等高线图中的函数值，如图2-38
 figure			%新建图形窗口
subplot(2,1,1)      %子图设置
 [c,h]=contour(z,4);
clabel(c,h)           %标注等高线图中的函数值
subplot(2,1,2)       %子图设置
contourf(z,4)		%如图2-39
