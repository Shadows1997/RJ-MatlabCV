alpha=0:0.01:2*pi;  %定义alpha的范围和刻度
x=sin(alpha);       %定义y与alpha的函数关系
y=cos(alpha);         %定义y与alpha的函数关系
plot(x,y)              %绘制函数x、y图形
axis([-1.5 1.5 -1.5 1.5])   %定义坐标轴范围
grid on            %为图形添加网格线
