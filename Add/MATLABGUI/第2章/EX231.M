%功能：罗盘图绘制函数compass使用实例
%********************************************************
 x=rand(2,5)         %设置x正态分布随机数
 y=rand(2,5)        %设置y正态分布随机数
subplot(2,1,1)     %子图设置
 plot(x,y,'ro')     %绘制图形
xlabel('X')
ylabel('Y')
 grid on               %添加网格
 subplot(2,1,2)      %子图设置
 compass(x,y)         %绘制罗盘图

