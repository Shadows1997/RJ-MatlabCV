%功能：向量场图绘制函数quiver使用实例
%********************************************************
 [x,y,z]=peaks(30);      %绘制peaks图形
 contour(x,y,z,5)         %绘制等高线
 [u,v]=gradient(z);      %设置梯度
 hold on                    %保持图形
 quiver(x,y,u,v)          %绘制向量场图
xlabel('X')
ylabel('Y')
