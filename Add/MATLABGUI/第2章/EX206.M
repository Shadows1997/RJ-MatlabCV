x=0:pi/100:2*pi;              %定义坐标轴x范围及刻度
y=sin(x);                        %定义y与x函数关系
line([0,2*pi],[0,0])          %绘制水平线
hold on;                          %保持图形
plot(x,y)                         %绘制函数图形
axis([0 2*pi -1 1])            %调整坐标轴范围参数
