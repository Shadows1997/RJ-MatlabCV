x=0:pi/20:2*pi;                %定义x坐标轴范围及刻度
y1=sin(x);                          %定义y与x函数关系 
y2=sin(x+pi/2);               %定义y2与x函数关系
plot(x,y1,'r:',x,y2,'+')    %绘制y1和y2，y1红色点线，y2用加号型线标记
legend('y1', 'y2')                 %说明y1、y2
xlabel('x')                           %坐标轴名称标注
ylabel('y')	                       %坐标轴名称标注

