x=0:pi/20:2*pi;               %定义x坐标轴范围及刻度
y1=sin(x);                       %定义y1与x函数关系
y2=sin(x+pi/2);               %定义y2与x之间的关系
plot(x,y1,'r:',x,y2,'+')   %显示图形
line([0,7],[0.5,0.5])      %加上水平线
line([0,7],[-0.5,-0.5])   %加上水平线
legend('y1','y2')