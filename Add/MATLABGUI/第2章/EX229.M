%功能：阶梯图绘制函数stairs使用实例
%********************************************************
t=0:0.5:7;                  %设置t的范围及刻度
y=exp(-0.2*t).*sin(t);  %定义y与x之间的函数关系
stairs(t,y)                 %绘制阶梯图
 hold on                     %保持图形
plot(t,y,'b')              %绘制普通二维图形
xlabel('X')
ylabel('Y')
