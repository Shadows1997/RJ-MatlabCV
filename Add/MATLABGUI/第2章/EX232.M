%功能：羽毛图绘制函数feather使用实例
%********************************************************
x=rand(2,5)       %定义x为正态分布随机数
y=rand(2,5)       %定义y为正态分布随机数
subplot(2,1,1)
 plot(x,y,'ro')        %绘制x与y的图形
xlabel('X')
ylabel('Y')
 subplot(2,1,2)   %子图设置
 grid on            %保持图形
 feather(x,y)     %羽毛图绘制
xlabel('X')
ylabel('Y')
