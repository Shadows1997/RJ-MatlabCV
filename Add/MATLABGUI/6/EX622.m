%********************************************************
%程序：EX622.M
%功能：把一幅真彩图像转换为一个HSV模型空间对应的图像
%********************************************************
RGB=imread('pic001.jpg');  %将图像格式文件读入为MATLAB图像对象数组数据
HSV=rgb2hsv(RGB);            %真彩图像转换为一个HSV模型空间对应的图像
I=HSV(:,:,1);
subplot(1,2,1); %子图设置
subimage(RGB);   %子图像显示
title('原图像');  %为当前图像显示标题
subplot(1,2,2);   %子图设置
subimage(I);       %子图像显示
title('变换後的图像');  %为当前图像添加标题
