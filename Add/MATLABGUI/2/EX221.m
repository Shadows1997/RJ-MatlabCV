%功能：极坐标绘图函数polar使用实例
%********************************************************
 t=0:0.01*pi:2*pi;                              %定义坐标轴范围及刻度
 r=2*sin(2*(t-pi/8)).*cos(2*(t-pi/8));    %定义r与t函数关系
 polar(t,r)                                        %绘制极坐标下的图形
