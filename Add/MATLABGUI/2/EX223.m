%功能：直方图绘制函数hist使用实例
%********************************************************
x=randn(500,1);     %定义x为正态分布随机数
y=randn(500,3);      %定义y为正态分布随机数
 subplot(3,1,2)      %子图设置
 hist(x)               %绘制x为变量的分布图
 subplot(3,1,3)      %子图绘制
 hist(x,100)          %绘制概率分布图
 subplot(3,1,1)      %子图绘制
 hist(y,25)            %绘制分布图
