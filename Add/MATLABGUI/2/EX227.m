 x=rand(1,5)     %定义x取值为正态分布随机数
 y=[0.2 0.45 0.1];  %定义y取值
 subplot(1,2,1)      %子图设置
 pie(x)                 %绘制以x为变量的饼状图
 subplot(1,2,2)      %子图设置
 pie(y)                 %绘制以y为变量的饼状图
