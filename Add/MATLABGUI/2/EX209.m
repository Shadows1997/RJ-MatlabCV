alpha=0:0.01:2*pi;    %定义alpha范围及刻度
x=sin(alpha);            %定义x与alpha的函数关系
y=cos(alpha);           %定义y与alpha的函数关系
plot(x,y)               %绘制x、y的函数图形
axis([-1.5 1.5 -1.5 1.5])    %调整坐标轴的范围参数
grid on
AxisRange=axis
