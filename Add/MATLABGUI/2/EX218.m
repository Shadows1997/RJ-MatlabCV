t=(1/16:1/8:1)'*2*pi; %定义八角形的刻度
x=sin(t);y=cos(t);     %定义函数关系
H=fill(x,y,'r');       %填充多边形
axis square              %坐标轴调整为正方形
set(H,'LineWidth',5)  %设置八角形的边线宽度
set(gcf,'color','w','Position',[400,350,250,150],'MenuBar','none')
set(gca,'Visible','off') %隐藏坐标轴    
