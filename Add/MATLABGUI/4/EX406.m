 [x,y]=meshgrid(-1:0.2:1);  %设置矩形网格
z=x.^2+sin(y*pi);             %定义函数关系
[m,n]=size(z);                 %变量为m、n的z矩阵
c=rand(m,n,3);                 %定义c、m、n之间的关系，产生均匀分布伪随机数
surf(x,y,z,c)                  %绘制三维曲面图
title('RGB coloring of surf')  %为当前图形添加标注
