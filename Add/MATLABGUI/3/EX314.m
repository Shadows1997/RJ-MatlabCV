%******************************************************
subplot(1,2,1)      %子图设置
[X,Y,Z]=cylinder;  %绘制单位圆柱体图
mesh(X,Y,Z)          %绘制三维图形
title('单位圆柱体')    %为当前图形添加标题
subplot(1,2,2)     %子图设置
t=1:10;              %设置t的范围
r(t)=t.*t;          %设置t与r之间的函数关系
[X,Y,Z]=cylinder(r,40);   %绘制圆柱体图形
mesh(X,Y,Z)                   %绘制三维图
title('一般圆柱体')           %为当前图形添加标题
