%功能：三维离散序列图绘制
%**********************************************
t=0:pi/10:6*pi;             %定义t的范围及刻度
x=exp(-t/10).*cos(t);     %定义x与t之间的函数关系
y=2*exp(-t/10).*sin(t);  %定义y与t之间的函数关系
stem3(x,y,t, 'filled')     %绘制三维离散序列图
hold on                        %保持图形
plot3(x,y,t)                 %绘制三维图形
xlabel('X')                  %设置x坐标轴的标注
ylabel('Y')                  %设置y坐标轴的标注
zlabel('Z')                  %设置z坐标轴的标注
