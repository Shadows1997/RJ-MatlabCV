%功能：三维曲线绘制函数plot3使用实例
%***********************************************************
 [X,Y]=meshgrid([-3:0.2:3]); %通过meshgrid创建网格数据
Z=X.*(-X.^2-Y.^2);              %设置z和y、x之间 的函数关系
plot3(X,Y,Z,'b')                %三维图形的绘制
Xlabel('x')                      %设置x坐标轴标注
Ylabel('y')                      %设置y坐标轴标注
Zlabel('z')                      %设置z坐标轴标注
