 %功能：质点轨迹动画使用实例
 %*************************************************  
 t=linspace(0,2*pi,1000);             %定义t的线型变量属性
 x=cos(t);                                 
 y=sin(t);                                  %定义x、y与t之间的函数关系
 plot(x,y),axis square,hold on;      %画出图形，以便比较comet是否跟着轨迹点
 comet(x,y,0.03)                           %质点轨迹动画
